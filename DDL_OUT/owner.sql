CREATE TABLE TEST_OWNER.DIM_TEST
( TEST_COLUMN_1                       INTEGER              NULL
, TEST_COLUMN_2                       VARCHAR2(20)         NULL
)
COMPRESS FOR OLTP
TABLESPACE TEST_DATA
;


GRANT DELETE, INSERT, SELECT, UPDATE ON TEST_OWNER.DIM_TEST TO TEST_JOBS;
GRANT DELETE, INSERT, SELECT, UPDATE ON TEST_OWNER.DIM_TEST TO TEST_OWNER_RW;
GRANT DELETE, INSERT, SELECT, UPDATE ON TEST_OWNER.DIM_TEST TO ETL_ADMIN;
