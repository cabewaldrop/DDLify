GRANT SELECT ON MTEST_OWNER.DIM_TEST TO MSTR_ADMIN;
GRANT SELECT, INSERT, UPDATE, ALTER, DELETE ON TEST_OWNER.DIM_TEST TO DEVELOPER_RW;

CREATE UNIQUE INDEX TEST_OWNER.IX_DIM_TEST_PK ON TEST_OWNER.DIM_TEST (TEST_COLUMN_1 ASC)
NOLOGGING
NOCOMPRESS
TABLESPACE TEST_DATA
;


ALTER TABLE TEST_OWNER.DIM_TEST ADD CONSTRAINT PK_DIM_TEST PRIMARY KEY (TEST_COLUMN_1) USING INDEX TEST_OWNER.IX_DIM_TEST_PK;


COMMENT ON TABLE  TEST_OWNER.DIM_TEST                                         IS 'Test Dimensional Table';
COMMENT ON COLUMN TEST_OWNER.DIM_TEST.TEST_COLUMN_1                           IS 'TEST COLUMN: COMMENT ONE';
COMMENT ON COLUMN TEST_OWNER.DIM_TEST.TEST_COLUMN_2                           IS 'TEST COLUMN: COMMENT TWO';

